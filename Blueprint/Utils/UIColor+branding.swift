import Foundation
import UIKit

extension UIColor {
    static var brandPrimary: UIColor {
        return UIColor(named: "BrandPrimary")!
    }
    static var brandPrimaryTransparent: UIColor {
        return UIColor(named: "BrandPrimaryTransparent")!
    }
    static var brandPrimaryDark: UIColor {
        return UIColor(named: "BrandPrimaryDark")!
    }
    static var brandPrimaryDarkTransparent: UIColor {
        return UIColor(named: "BrandPrimaryDarkTransparent")!
    }
    static var brandAccent: UIColor {
        return UIColor(named: "BrandAccent")!
    }
    static var brandOffWhite: UIColor {
        return UIColor(named: "BrandOffWhite")!
    }
}
